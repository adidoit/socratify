import { z } from 'zod';

export const TrackModelSchema = z.object({
  id: z.number().int(),
  trackUuid: z.string(),
  clerkUserId: z.string(),
  userGoal: z.string(),
  userGoalText: z.string().optional(),
  numItems: z.number().int(),
  status: z.string(),
  createdAt: z.date(),
  updatedAt: z.date(),
  poolSize: z.number().int().optional(),
  sampleSize: z.number().int().optional(),
  trackName: z.string(),
  trackCoverImage: z.string().optional(),
  errorMessage: z.string().optional(),
  generationCompletedAt: z.date().optional(),
  generationStartedAt: z.date().optional(),
  generationStatus: z.string().optional(),
  progressPercent: z.number().int(),
  retryCount: z.number().int(),
  statusMessage: z.string().optional(),
  workflowRunId: z.string().optional(),
  visibility: z.string(),
  forkedFromUuid: z.string().optional(),
  clerkOrganizationId: z.string().optional(),
  metadata: z.any(),
});
